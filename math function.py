import math
import random

num = -4.456
# round the number
print(round(num))

# round the number to lower
print(math.floor(num))

# round the num  up
print(math.ceil(num))

# make -ve num a +ve num
print(abs(num))

# calculate square root
print(math.sqrt(abs(num)))

# raise the number by a power
print(pow(4, 2))

# find max_num also takes list as argumen
print(max(1, 100, 34, 123))
# find min_num as well
print(min(-67, 100, 90, 45))
